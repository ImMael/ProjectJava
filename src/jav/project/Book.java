package jav.project;

public class Book {
    String title;
    String author;
    String release;
    String column;
    String row;
    String pitch;

    public Book(String title, String author, String release, String column, String row, String pitch) {
        this.title = title;
        this.author = author;
        this.release = release;
        this.column = column;
        this.row = row;
        this.pitch = pitch;
    }



}
